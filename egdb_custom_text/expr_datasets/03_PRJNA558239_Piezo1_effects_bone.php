<p>
  Condition: Transcriptional analysis of the effects of Piezo1 deletion in bone
  Genotype: Piezo1fl/fl;Prx1-Cre (both heterozygotes and homozygotes were used since they did not show any difference)
  Phenotype: Mice lacking of Piezo1 showed shorter bones and osteoporosis-like phenotyphe with a decreased in bone thickness, mass and volume. This lost of bone was independent of the gender
  Tissue: Bone
  Age: 6 week/1 month and 2 week old
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Piezo1_Wt: Control mice (4 biological replicates, with 1 run each one).</li>
  <li>Piezo1_ko: Mice knock out for Piezo1, Piezo1fl/fl;Prx1-Cre (4 biological replicates, with 1 run each one).</li>
</ul>

<p>
  Total mRNA from cortical bones of 6-week WT and Prx1Cre; Piezo1fl/fl mice were collected and grinded with liquid nitrogen to extract the RNA. RNA was harvested using Trizol reagent. Illumina TruSeq RNA Sample Prep Kit was used for the construction of sequencing libraries.
  Libraries were prepared according to Illumina's instructions.
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 2500 sequencer and normalized to TPM.
</p>
