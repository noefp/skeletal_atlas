<h3>Effects of Piezo1 deletion in bone</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Piezo1 deletion in bone.<br>
  <b>Genes:</b> Piezo1 encodes a mechanically-activated ion channel gene related with the sensing of mechanical forces by cells.<br>
  <b>Phenotype:</b> Mice lacking of Piezo1 showed shorter bones and osteoporosis-like phenotyphe with a decreased in bone thickness, mass and volume. This lost of bone was independent of the gender.<br>
  <b>Tissue:</b> Femora.<br>
  <b>Age:</b> 8 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 150 bp.<br>
  <b>Sample processing:</b> Total mRNA from cortical bones of 6-week WT and Prx1<sup>Cre</sup>Piezo1<sup>fl/fl</sup> mice were collected and grinded with liquid nitrogen to extract the RNA. RNA was harvested using Trizol reagent. Illumina TruSeq RNA Sample Prep Kit was used for the construction of sequencing libraries.
    Libraries were prepared according to Illumina's instructions.
    cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 2500 sequencer and normalized to TPM.
  
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_Piezo1: Control mice (4 biological replicates, with 1 run each one).</li>
  <li>Piezo1_cko: Mice conditional knock out for Piezo1, Prx1<sup>Cre</sup>Piezo1<sup>fl/fl</sup> (4 biological replicates, with 1 run each one).</li>
</ul>

