<p>
  Condition: Transcriptional analysis of the effects of VitC supplementation in bone from Gulo-/- mice.
  Genotype: Gulo-/- 
  Phenotype: Mice lacking of enough VitC exhibited severe defects in bone structure and mechanical strength
  Tissue: Bone
  Age: 20 week/4 month and 4 week old
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>VitC_Wt: Control mice, Gulo+/+ (3 biological replicates, with 1 run each one).</li>
  <li>VitC_Gulotm1Mae_15weeks: Mice knock out for Gulo, Gulo -/-, with not enough VitC supplementation (3 biological replicates, with 1 run each one).</li>
  <li>VitC_Gulotm1Mae_15weeks: Mice knock out for Gulo, Gulo -/-, with not enough VitC supplementation (3 biological replicates, with 1 run each one).</li>
</ul>

<p>
  20 week old femoral bones from Wt and Gulo-/- mice with normal or defficiency of VitC supplementation were dissected, bone marrow was flushed, periost was removed and remaining bone tissue was thorogly washed in ice cold PBS.
  RNA was extraced with commercially available extraction kits (Zymo). RNA-Seq libraries were prepared using the Illumina TruSeq v2 library preparation kit and sequenced with paired-end 50â€‰bp reads 
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 4000 sequencer and normalized to TPM.
</p>
