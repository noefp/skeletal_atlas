<h3>Effects of VitC supplementation in bone</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of vitamin C supplementation in bone from Gulo<sup>-/-</sup> mice.<br>
  <b>Genes:</b> Gulo, a gene implied in the synthesize of vitamin C.<br>
  <b>Phenotype:</b> Mice lacking of enough vitamin C exhibited severe defects in bone structure and mechanical strength.<br>
  <b>Tissue:</b> Femora.<br>
  <b>Age:</b> 24 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 50 bp.<br>
  <b>Sample processing:</b> 20 week old femoral bones from Wt and Gulo<sup>-/-</sup> mice with normal or defficiency of VitC supplementation were dissected, bone marrow was flushed, periost was removed and remaining bone tissue was thorogly washed in ice cold PBS.
  RNA was extraced with commercially available extraction kits (Zymo). RNA-Seq libraries were prepared using the Illumina TruSeq v2 library preparation kit and sequenced with paired-end 50â€‰bp reads 
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 4000 sequencer and normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_VitC: Control mice, Gulo<sup>+/+</sup> (3 biological replicates, with 1 run each one).</li>
  <li>VitC_Gulotm1Mae_15weeks: Mice knock out for Gulo, Gulo<sup>-/-</sup>, with not enough VitC supplementation (3 biological replicates, with 1 run each one).</li>
  <li>VitC_Gulotm1Mae_20weeks: Mice knock out for Gulo, Gulo<sup>-/-</sup>, with enough VitC supplementation (3 biological replicates, with 1 run each one).</li>
</ul>

