<p>
  Condition: Transcriptome analysis of the effects of meniscus tears in ACL injury
  Tissue: Anterior Cruciate Ligament (ACL)
  Age: Between the twenties and thirties
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>ACL.injury.only: Patients suffering only ACL injury (10 biological replicates, with 3 runs each one).</li>
  <li>ACL.injury_Meniscus.tear: Patients suffering from both, ACL injury and meniscus tears (18 biological replicates, with 3 runs each one).</li>
</ul>

<p>
ACL remnants were homogenized with Trizol reagent (Invitrogen, Carlsbad, CA) and the use of a Polytron homogenizer (Kinematica AG, Lucerne, Switzerland). Aliquot of the homogenized suspension was transferred to a microfuge tube and incubated at room temperature for 5 min to permit the complete dissociation of nucleoprotein complexes. After addition of chloroform, the sample was mixed vigorously and the contents were transferred to phase lock gel tube and centrifuged for 15 min at 4°C. 
The clear aqueous layer was transferred to a fresh tube and one volume of 70% ethyl alcohol was added to precipitate RNA. RNA was collected using RNeasy spin columns (Qiagen Inc., Valencia, CA) according to the instruction manual. RNA was quantified by using a NanoDrop ND-100 spectrophotometer (NanoDrop) while quality was assessed with the use of an Agilent Bioanalyzer (Agilent Technologies, Santa Clara, CA). Libraries were prepared using Sigma SeqPlex (Sigma–Aldrich, St. Louis, MO) kit according to manufacturer's protocol.
cDNA libraries were sequenced by SE sequencing with an Illumina HiSeq 3000 sequencer and normalized to TPM
