<p>
  Condition: Transcriptional analysis of the role of Byglican in bone development and regeneration after fracture
  Genotype: Bgn –/0 (Bgn is located in X chromosome) 
  Effects in tissue: Mice lacking of Bgn showed a delay in growth and reduction in cortical thickness and trabecular number, and an increase in trabecular spacing and bone mineral content (BMC). Analysis of serum after suffering a bone fracture showed reduction in monocyte chemoattractant protein 1 (MCP-1) and IL-6 secretion and slow recovering signs in bone
  Tissue: Bone
  Age: 6-7 week/1 month and 2-3 week old
  Sex: Males

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>No_Fracture_Wt: Control mice without suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>No_Fracture_Bgn_ko: Mice lacking of Bgn gene, Bgn –/0 (males), without suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>Fracture_Wt: Control mice suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>Fracture_Bgn_ko: Mice lacking of Bgn gene, Bgn –/0 (males), suffering fracture (6 biological replicates, with 1 run each one).</li>

</ul>

<p>
  Femoral shafts were isolated and frozen in liquid nitrogen. The bones were centered in a tissue tube (Covaris) under liquid nitrogen and crushed using a CP02 cryoPREP Dry Pulverizer. Total mRNA was extracted and purified from the pulverized tissue using TriPure (Sigma, United States) followed by RNeasy mini kit (Qiagen) hybrid protocol.
  RNA was transcribed by Superscript IV (Thermo Fisher Scientific) and full-length 2nd strand cDNA amplified by LongAmp DNA polymerase (New England BioLabs). Sequencing libraries were prepared using a Nextera XT kit (Illumina). Libraries were sequenced using an Illumina NextSeq500 or NextSeq2000 sequencer and normalized to TPM.
  
