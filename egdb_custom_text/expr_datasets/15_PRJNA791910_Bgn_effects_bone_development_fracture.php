<h3>Role of Bgn in bone development and regeneration</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the role of Byglican in bone development and regeneration after fracture.<br>
  <b>Gene:</b> Biglycan (Bgn) is a member of the SLRP family and is highly abundant in the ECM of a variety of tissues. During skeletal development, high levels of Bgn mRNA are detected in areas of endochondral and membranous bone formation.<br>
  <b>Phenotype:</b> Mice lacking of Bgn showed a delay in growth and reduction in cortical thickness and trabecular number, and an increase in trabecular spacing and bone mineral content (BMC). Analysis of serum after suffering a bone fracture showed reduction in monocyte chemoattractant protein 1 (MCP-1) and IL-6 secretion and slow recovering signs in bone.<br>
  <b>Tissue:</b> Femorae.<br>
  <b>Age:</b> 8 week old.<br>
  <b>Sex:</b> Males.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 37 bp.<br>
  <b>Sample processing:</b> Femoral shafts were isolated and frozen in liquid nitrogen. The bones were centered in a tissue tube (Covaris) under liquid nitrogen and crushed using a CP02 cryoPREP Dry Pulverizer. Total mRNA was extracted and purified from the pulverized tissue using TriPure (Sigma, United States) followed by RNeasy mini kit (Qiagen) hybrid protocol.
  RNA was transcribed by Superscript IV (Thermo Fisher Scientific) and full-length 2nd strand cDNA amplified by LongAmp DNA polymerase (New England BioLabs). Sequencing libraries were prepared using a Nextera XT kit (Illumina). Libraries were sequenced using an Illumina NextSeq500 or NextSeq2000 sequencer and normalized to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_No_Fracture: Control mice without suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>No_Fracture_Bgn_ko: Mice lacking of Bgn gene, Bgn<sup>–/0</sup> (males), without suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>Wt_Fracture: Control mice suffering fracture (6 biological replicates, with 1 run each one).</li>
  <li>Fracture_Bgn_ko: Mice lacking of Bgn gene, Bgn<sup>–/0</sup> (males), suffering fracture (6 biological replicates, with 1 run each one).</li>

</ul>

