<h3>Role of Hnf4α in bone</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of osteoblast and osteocytes specific deletion of Hnf4α in bone.<br>
  <b>Gene:</b> Hepatocyte nuclear factor 4α (HNF4α) is a highly conserved transcription factor. HNF4α is constitutively localized in the nucleus and does not require binding of a ligand to homodimerize and interact with the response elements of its target genes. HNF4α can function as an activator or repressor of genes involved in cell metabolic activity, transport, glucose and lipid homeostasis, and detoxification of xenobiotic.<br>
  <b>Phenotype:</b> Since birth, the mice were smaller and hypomineralized. In bones, trabecular part was reduced compared with control littermates. In males, cortical bone was not different, but females suffered a reduction during their growth.<br>
  <b>Tissue:</b> Tibiae.<br>
  <b>Age:</b> 8 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Single.<br>
  <b>Reads length:</b> 50 bp.<br>
  <b>Sample processing:</b> RNA from the tibiae of 6 week-old male mice with the epyphises removed and the marrow flushed was isolated RNA from tissues and from cell cultures using TRI Reagent (MRC) and purified RNA using an RNeasy kit (Qiagen).
  Libraries were prepared using the TruSeq Total RNA-Seq Library Preparation Kit (Illumina) following the manufacture’s instructions. Then sequencing was performed by SE with an Illumina HiSeq 4000 sequencer and normalised to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_HNF4a: Control mice without Hnf4α deletion (4 biological replicates, with 1 run each one).</li>
  <li>HNF4a_cko: Mice with conditional deletion of Hnf4α gene, Bglap<sup>Cre</sup>Hnf4α<sup>fl/fl</sup> (4 biological replicates, with 1 run each one).</li>
  
</ul>

