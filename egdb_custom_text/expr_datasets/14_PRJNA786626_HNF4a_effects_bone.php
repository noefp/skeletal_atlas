<p>
  Condition: Transcriptional analysis of osteoblast and osteocytes specific deletion of Hnf4α in bone
  Genotype: Hnf4αflox/flox;BGLAP-Cre (conditional knock out in osteoblasts)
  Phenotype: Since birth, the mice were smaller and hypomineralized. In bones, trabecular part was reduced compared with control littermates. In males, cortical bone was not different, but females suffered a reduction during their growth
  Tissue: Bone
  Age: 6 week/1 month and 2 week old
  Sex: Males

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>HNF4a_Wt: Control mice without Hnf4α deletion (4 biological replicates, with 1 run each one).</li>
  <li>HNF4a_ko: Mice with conditional deletion of Hnf4α gene, Hnf4αflox/flox;BGLAP-Cre (4 biological replicates, with 1 run each one).</li>
  
</ul>

<p>
  RNA from the tibiae of 6 week-old male mice with the epyphises removed and the marrow flushed was isolated RNA from tissues and from cell cultures using TRI Reagent (MRC) and purified RNA using an RNeasy kit (Qiagen).
  Libraries were prepared using the TruSeq Total RNA-Seq Library Preparation Kit (Illumina) following the manufacture’s instructions. Then sequencing was performed by SE with an Illumina HiSeq 4000 sequencer and normalised to TPM.
   
  
