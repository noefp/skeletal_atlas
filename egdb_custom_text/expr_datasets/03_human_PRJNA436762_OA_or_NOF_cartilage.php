<p>
  Condition: Transcriptome analysis from hip cartilage of patients with Neck Of femur Fracture (NOF) or Osteoarthritis (OA) undergoing acetabulofemoral joint replacement
  Tissue: Hip cartilage
  Age: Range from 70-95 year old (NOF) and 50-85 (OA)
  Sex: Women

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Healthy_NOF_fracture: Patients with healthy cartilage but suffering Neck Of femur Fracture (NOF) (6 biological replicates, with 1 run each one).</li>
  <li>OA_hip_cartilage: Patients suffering Osteoarthritis (OA) in hip cartilage (10 biological replicates, with 1 run each one).</li>
  
</ul>

<p>
  Cartilage samples were ground into powder and homogenized using Invitrogen TRIzol Reagent after which RNA was isolated by RNeasy Kit
  cDNA libraries were prepared for sequencing using Illumina TrueSeq mRNA kits with the manufacturersâ€™ protocols.
  The cDNA libraries were sequenced by PE sequencing using an Illumina HiSeq 3000 sequencer and normalized to TPM.
</p>
