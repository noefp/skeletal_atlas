<h3>Role of Kindlin-2 in osteoarthritis</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Kindlin-2 gene in cartilage.<br>
  <b>Gene:</b> Kindlins genes codify family of adaptor proteins that are recruited to integrin-containing adhesion sites, termed focal adhesions, Kindlin-2 is ubiquitously expressed.<br>
  <b>Phenotype:</b> Mice lacking of Kindlin2 gene showed early development and exacerbated signs of osteoarthritis.<br>
  <b>Tissue:</b> Articular cartilage.<br>
  <b>Age:</b> 20 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 150 bp.<br>
  <b>Sample processing:</b> Total RNA was extracted from homogenized articular cartilage tissues of control and cKO mice at 5 months after tamoxifen injections using a TransZol Up Plus RNA Kit (Transgen, China). Sequencing libraries were generated using NEBNext R UltraTM RNA Library Prep Kit (Illunina, NEB, United States) and the library quality was assessed on the Agilent Bioanalyzer 2100 system. 
  After cluster generation, the library preparations were sequenced and 150 bp paired-end reads were generated. Libraries were sequenced using an Illumina NovaSeq 6000 sequencer and normalized to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_Kindlin2: Control mice (3 biological replicates, with 1 run each one).</li>
  <li>Kindlin2_cko: Mice lacking of Kindlin2 gene, Aggrecan<sup>CreERT2</sup>K2<sup>fl/fl</sup> (3 biological replicates, with 1 run each one).</li>

</ul>

