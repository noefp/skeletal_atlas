<p>
  Condition: Role of kindlin-2 in osteoarthritis
  Genotype: K2fl/fl;Aggrecan-CreERT2 (conditionally induced knock out) 
  Phenotype: Mice lacking of Kindlin-2 gene showed early development and exacerbated signs of osteoarthritis 
  Tissue: Articular cartilage
  Age: 20 week/5 month old
  Sex: Males

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Kindlin-2_Wt: Control mice (3 biological replicates, with 1 run each one).</li>
  <li>Kindlin-2_ko: Mice lacking of Kindlin-2 gene, K2fl/fl;Aggrecan-CreERT2 (3 biological replicates, with 1 run each one).</li>

</ul>

<p>
  Total RNA was extracted from homogenized articular cartilage tissues of control and cKO mice at 5 months after tamoxifen injections using a TransZol Up Plus RNA Kit (Transgen, China). Sequencing libraries were generated using NEBNext R UltraTM RNA Library Prep Kit (Illunina, NEB, United States) and the library quality was assessed on the Agilent Bioanalyzer 2100 system. 
  After cluster generation, the library preparations were sequenced and 150 bp paired-end reads were generated. Libraries were sequenced using an Illumina NovaSeq 6000 sequencer and normalized to TPM.
  
</p>
