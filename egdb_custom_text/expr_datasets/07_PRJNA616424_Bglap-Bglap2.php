<h4>Experimental Conditions:</h4>
<ul>
  <li>Bglap-Bglap2_Wt: Control mice (3 biological replicates, with 1 run each one).</li>
  <li>Bglap-Bglap2_ko: Mice knock out for Osteocalcin, Bglap -/- and Bglap2 -/- (5 biological replicates, with 1 run each one).</li>
</ul>

<p>
  RNA was extracted from cortical tibial bone and libraries were constructed using the SMART-Seq v4 UltraLow RNA Input Kit. Library construction was completed using the ThruPLEX DNA-seq kit.
  cDNA libraries were sequenced by PE sequencing with an Illumina NextSeq 550 sequencer and normalized to TPM.
