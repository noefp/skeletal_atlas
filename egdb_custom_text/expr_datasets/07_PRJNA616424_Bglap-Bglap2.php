<h3>Effects of Osteocalcein deletion in bone</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Osteocalcein (Bglap and Bglap2) deletion in bone.<br>
  <b>Genes:</b> Bglap and Bglap2 are highly abundant bone proteins secreted by osteoblasts that regulate bone remodeling and energy metabolism.<br>
  <b>Phenotype:</b> Not provided.<br>
  <b>Tissue:</b> Tibiae.<br>
  <b>Age:</b> 16 week old.<br>
  <b>Sex:</b> Unknown.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 75 bp.<br>
  <b>Sample processing:</b> RNA was extracted from cortical tibial bone and libraries were constructed using the SMART-Seq v4 UltraLow RNA Input Kit. Library construction was completed using the ThruPLEX DNA-seq kit.
  cDNA libraries were sequenced by PE sequencing with an Illumina NextSeq 550 sequencer and normalized to TPM.

</p>

<h4>Cases:</h4>
<ul>
    <li>Wt_Bglap-Bglap2: Control mice (3 biological replicates, with 1 run each one).</li>
    <li>Bglap-Bglap2_ko: Mice knock out for Osteocalcin, Bglap<sup>-/-</sup> and Bglap2<sup>-/-</sup> (5 biological replicates, with 1 run each one).</li>
  </ul>

