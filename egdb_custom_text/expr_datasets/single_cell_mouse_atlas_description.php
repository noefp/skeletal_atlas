<h2>Single cell Basic Atlas for <i>Mus musculus</i></h2>

<h3>Cartilaginous lineage</h3>
<p>
    This dataset was published by <a href="https://doi.org/10.1172/jci.insight.171984" target="_blank">Zieba J et al., 2023</a> 
    and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/PRJNA967698" target="_blank">PRJNA967698</a>.<br>
    Cells source: Tibial and femoral cartilage growth plate chondrocytes.<br>
    Age: Postnatal day 5.<br>
    Sex: Both.<br>
    Layout: PAIRED.<br>
</p>
<h3>Osteoclasts and myocytes</h3>
<p>
    This dataset was published by <a href=" https://doi.org/10.1111/imcb.12718" target="_blank">Rueda AD et al., 2024</a> 
    and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/PRJNA971209" target="_blank">PRJNA971209</a>.<br>
    Cells source: Humerus, radius and ulna.<br>
    Age: Postnatal day 1.<br>  
    Sex: Both.<br>
    Layout: PAIRED.<br>

</p>
<h3>Osteogenic and hematopoietic lineages</h3>
<p>
    This dataset was published by <a href="https://doi.org/10.1016/j.jbc.2024.107158" target="_blank">Nookaew I et al., 2024</a> 
    and raw data can be found in the BioProject <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA1080020" target="_blank">PRJNA1080020</a>.<br>
    Cells source: Multiple tissues. Integration of different scRNA-seq datasets with their own<br>
    Age: Variable.<br>
    Sex: Both.<br>
    Layout: PAIRED.<br>
    Datasets integrated: Different scRNA-seq datasets used for integration came from: <a href="https://doi.org/10.1038/s41556-019-0439-6" target="_blank">Baccin C et al., 2020</a>, <a href="https://doi.org/10.1016/j.cell.2019.04.040" target="_blank">Baryawno N et al., 2019</a>, <a href="https://doi.org/10.1038/s41586-019-1104-8" target="_blank">Tikhonova AN et al., 2019</a>, <a href=" https://doi.org/10.7554/eLife.54695" target="_blank">Zhong L et al., 2020</a>, <a href="https://doi.org/10.1038/s41467-019-14029-w" target="_blank">Matsushita Y et al., 2020</a>, <a href="https://doi.org/10.1016/j.celrep.2021.109352" target="_blank">Sivaraj KK et al., 2021</a>, <a href="https://doi.org/10.1002/jbmr.4052" target="_blank">Ayturk UM et al., 2021</a>, <a href="https://doi.org/10.1016/j.celrep.2020.107871" target="_blank">Holmes G et al., 2020</a>, <a href="https://doi.org/10.1038/s41467-021-26571-7" target="_blank">Wang, JS et al., 2021</a>.


