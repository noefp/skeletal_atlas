<h3>Effects of deletion of EphrinB2 in osteocytes</h3>
<p>
  <b>Experimental condition:</b> Transcription profile of specific deletion of EphrinB2 in osteocytes (Dmp1<sup>Cre</sup>EfnB2<sup>fl/fl</sup>).<br>
  <b>Genes:</b> EphrinB2 (EfnB2), this gene encodes a member of the ephrin (EPH) family, precisely from ephrin-B (EFNB) class, which are transmembrane proteins. The ephrins and EPH-related receptors comprise the largest subfamily of receptor protein-tyrosine kinases.<br>
  <b>Phenotype:</b> EphrinB2 conditional knock out mice showed brittle bones. However, they did not show low bone mass, but defective bone material.<br>
  <b>Tissue:</b> Femora.<br>
  <b>Age:</b> 12 week old.<br>
  <b>Sex:</b> Females.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads lenght:</b> Paired.<br>
  <b>Sample processing:</b> Femora were collected, ends removed and marrow flushed out prior to snap freezing in liquid nitrogen. The cortical bone was homogenized in QIAzol lysis reagent at 4 degrees C using a Polytron PTA 20S homogenised. RNA was extracted using a Rneasy Lipid Tissue Mini kit (Qiagen) according to manufacturer's instructions.
    mRNA reverse transcription cDNA libraries were prepared using the TruSeq RNA Sample preparation kit (Illumina) following the manufacturerâ€™s instructions.
    cDNA libraries were sequenced by PE sequencing (100 x 2) with an Illumina HiSeq 2500 sequencer and normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
    <li>Wt_EphrinB2: Control mice (4 biological replicates, with 3 runs each one).</li>
    <li>EphrinB2_cko: EphrinB2 conditional knock out mice (Dmp1<sup>Cre</sup>EfnB2<sup>fl/fl</sup>) (3 biological replicates, with 3 runs each one).</li>
</ul>

