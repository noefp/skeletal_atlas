<p>
  Condition: Transcription profile of specific deletion of EphrinB2 in osteocytes (Dmp1-Cre:EfnB2f/f).
  Phenotype: EphrinB2 knock out mice showed brittle bones. However, they did not show low bone mass, but defective bone material
  Tissue: Bone
  Age: 12 week/3 month old 
  Sex: Females

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>EphrinB2_Wt: Control mice (4 biological replicates, with 3 runs each one).</li>
  <li>EphrinB2_ko: EphrinB2 knock out mice, Dmp1-Cre:EfnB2f/f (3 biological replicates, with 3 runs each one).</li>
</ul>

