<h3>Role of Tet genes in bone</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Tet1 and Tet2 deletion in bone.<br>
  <b>Genes:</b> Ten Eleven Translocation (TET), enzymes that oxidize 5-methylcytosines (5mCs) and promote locus-specific reversal of DNA methylation.<br>
  <b>Phenotype:</b> Mice lacking of Tet1 and Tet2 (enzymes that modify DNA producing 5hmC) showed severe skeletal defects mimicking bone phenotypes of Vitamin C-insufficient, Gulo knockout mice, a model of Vitamin C deficiency and scurvy.<br>
  <b>Tissue:</b> Femora.<br>
  <b>Age:</b> 11 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 50 bp.<br>
  <b>Sample processing:</b>11 week old femoral bones from Prrx1-Cre driven Tet1fl/fl, Tet2fl/fl or Tet1fl/fl and Tet2 fl/fl mice were dissected, bone marrow was flushed, periost was removed and remaining bone tissue was thorogly washed in ice cold PBS.
  RNA was extraced with commercially available extraction kits (Zymo). RNA-Seq libraries were prepared using the Illumina TruSeq v2 library preparation kit and sequenced with paired-end 50â€‰bp reads 
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 4000 sequencer and normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_Tet: Control mice, Prrx1<sup>Cre+</sup> (3 biological replicates, with 1 run each one).</li>
  <li>Tet1_cko: Mice conditional knock out for Tet1, Prrx1<sup>Cre+</sup>Tet1<sup>fl/fl</sup> (3 biological replicates, with 1 run each one).</li>
  <li>Tet2_cko: Mice conditional knock out for Tet2, Prrx1<sup>Cre+</sup>Tet2<sup>fl/fl</sup> (3 biological replicates, with 1 run each one).</li>
  <li>Tet1-2_cko: Mice conditional knock out for Tet1 and Tet2, Prrx1<sup>Cre+</sup>Tet1/2<sup>fl/fl</sup> (3 biological replicates, with 1 run each one).</li>
</ul>

