<p>
  Condition: Transcriptional analysis of the effects of Tet1 and Tet2 deletion in bone
  Genotype: Prrx1-Cre+;Tet1fl/fl Prrx1-Cre+;Tet2fl/fl Prrx1-Cre+;Tet1/2fl/fl
  Phenotype: Mice lacking of Tet1 and Tet2 (enzymes that modify DNA producing 5hmC) showed severe skeletal defects which mimic bone phenotypes of Vitamin C-insufficient, Gulo knockout mice, a model of Vitamin C deficiency and scurvy.
  Tissue: Bone
  Age: 11 week/2 month and 3 week old
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Tet_Wt: Control mice, Prrx1-Cre+ (3 biological replicates, with 1 run each one).</li>
  <li>Tet_Tet1_ko: Mice knock out for Tet1, Prrx1-Cre+;Tet1fl/fl (3 biological replicates, with 1 run each one).</li>
  <li>Tet_Tet2_ko: Mice knock out for Tet2, Prrx1-Cre+;Tet2fl/fl (3 biological replicates, with 1 run each one).</li>
  <li>Tet_Tet1-2_ko: Mice knock out for Tet1 and Tet2, Prrx1-Cre+;Tet1/2fl/fl (3 biological replicates, with 1 run each one).</li>
</ul>

<p>
  11 week old femoral bones from Prrx1-Cre driven Tet1fl/fl, Tet2fl/fl or Tet1fl/fl and Tet2 fl/fl mice were dissected, bone marrow was flushed, periost was removed and remaining bone tissue was thorogly washed in ice cold PBS.
  RNA was extraced with commercially available extraction kits (Zymo). RNA-Seq libraries were prepared using the Illumina TruSeq v2 library preparation kit and sequenced with paired-end 50â€‰bp reads 
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 4000 sequencer and normalized to TPM.
