<h3>Evaluation of pretreatment with IL-27 in osteomyelitis</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of mice suffering osteomyelitis with or without IL-27 pretreatment.<br>
  <b>Surgical procedure:</b> Mice were intramuscularly injected with recombinant adeno-associated virus expressing murine IL-27 (rAAV-IL-27) or recombinant adeno-associatd virus expressing GFP (rAAV-GFP, control) and then challenged with a S. aureus contaminated trans-tibial implant. Subsequently, infected tibia were collected on days 1, 3, 7 and 14 post-septic surgery for RNA sequencing.<br>
  <b>Phenotype:</b> At early stage of the infection, IL-27 is highly expressed in osteoblasts and macrophages but not in osteoclasts where the pathogen can persist. Pretreatment with IL-27 prevents abceses formation and reduce bone loss due to osteomyelitis.<br>
  <b>Tissue:</b> Tibiae.<br>
  <b>Age:</b> 8 week old.<br>
  <b>Sex:</b> Females.<br>
  <b>Layout:</b> Single.<br>
  <b>Reads length:</b> 100 bp.<br>
  <b>Sample processing:</b> Tibiae were pulverized at liquid nitrogen temperature and homogenized using Bullet Blender Gold (Next Advance). Collection of Total RNA from homogenized tibia was performed using TRIzol extraction (ThermoFisherScientific) and RNeasy Mini Kits (Qiagen).
  The TruSeq Stranded Total RNA Library Prep Gold (Illumina) was utilized for next generation sequencing library preparation per the manufactureâ€™s instructions. Then sequencing was performed by SE with an Illumina NovaSeq 6000 sequencer and normalised to TPM.

</p>

