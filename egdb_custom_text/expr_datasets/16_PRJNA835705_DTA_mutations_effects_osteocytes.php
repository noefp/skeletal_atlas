<h3>Effects of premature osteocytes death in bone</h3>
<p>
  <b>Experimental condition:</b> Transacriptional analysis of the effects of partial ablation of DTA in osteocytes.<br>
  <b>Procedure:</b> Use of a mouse model of conditional deletion of osteocytes by the expression of diphtheria toxin subunit α (DTA) in dentin matrix protein 1 (DMP1)-positive osteocytes.<br>
  <b>Phenotype:</b> Mice heterozygotes for DTA showed a delay in growth, loss of bone mass, kyphosis and deformities as they get older. Had more empty lacunae without the presence of osteocytes within cortical and trabecular bone matrix and impairment of osteocyte network. These changes were gender insensitive.<br>
  <b>Tissue:</b> Femorae.<br>
  <b>Age:</b> 8 week old.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 92-141 bp.<br>
  <b>Sample processing:</b> Total RNA of whole cortical bone with bone marrow flushed out from 4-week WT and DTAhet mice was extracted using TRIzol reagent (Thermo Fisher), quantified and purified using Bioanalyzer 2100 and RNA 6000 Nano LabChip Kit (Agilent). 
  Following purification, mRNA library were constructed following the vendor’s recommended protocol and sequencing was performed by PE with an Illumina NovaSeq 6000 sequencer and normalised to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
    <li>Wt_DTA: Control mice (2 biological replicates, with 1 run each one).</li>
    <li>DTA_heterozygous: Mice heterozygous from DTA gene, Dmp1<sup>Cre</sup>DTA<sup>fl/+</sup> (2 biological replicates, with 1 run each one).</li>
</ul>

