<p>
  Condition: Transcriptional analysis of the effects of Oscar deletion in osteoarthritis in a Destabilization of the Medial Meniscus (DMM) model
  Genotype: Oscar-/- 
  Surgical procedure: Destabilization of the Medial Meniscus (DMM)
  Phenotype: Mice lacking of Oscar gene showed a delay in osteoarthritis development 
  Tissue: Articular cartilage
  Age: 10 weeks + 2 or 4 weeks after surgery/2 month and 2 week old
  Sex: Males

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>OA_Wt_sham_2weeks: Control mice, sham condition, 2 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>OA_Wt_sham_4weeks: Control mice, sham condition, 4 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>OA_Wt_dmm_2weeks: Control mice, DMM condition, 2 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>OA_Wt_dmm_4weeks: Control mice, DMM condition, 4 weeks after surgery (1 biological replicate, with 1 run each one).</li> 
  <li>OA_Oscar-ko_sham_2weeks: Mice knock out for Oscar, Oscar -/-, sham condition, 2 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_sham_4weeks: Mice knock out for Oscar, Oscar -/-, sham condition, 4 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_dmm_2weeks: Mice knock out for Oscar, Oscar -/-, DMM condition, 2 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_dmm_4weeks: Mice knock out for Oscar, Oscar -/-, DMM condition, 4 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
</ul>

<p>
  Articular cartilage mRNA profiles of 10-weeks-old wild-type (WT) and Oscar knockout (Oscar−/−) mice were performed for the destabilization of the medial meniscus (DMM) or sham surgery and each cartilage tissue sample was harvested from two time-points (2- and 4-weeks after surgery). Sequencing libraries were prepared according to the manufacturer’s instructions (TruSeq Stranded mRNA Library Prep Kit; Illumina, San Diego, CA, USA).
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 2500 sequencer and normalized to TPM.
</p>
