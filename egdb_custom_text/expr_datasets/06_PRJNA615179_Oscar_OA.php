<h3>Oscar deletion in osteoarthritis</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Oscar deletion in osteoarthritis, precisely in a Destabilization of the Medial Meniscus (DMM) model.<br>
  <b>Genes:</b> Oscar is an immunoglobulin (Ig)-like activating receptor of the leukocyte receptor complex that is specifically expressed on pre-osteoclasts. An activating receptor for collagen that co-stimulates osteoclast differentiation.<br>
  <b>Phenotype:</b> Mice lacking of Oscar gene showed a delay in osteoarthritis development.<br>
  <b>Tissue:</b> Articular cartilage.<br>
  <b>Age:</b> 10 week old + 2 or 4 weeks after surgery.<br>
  <b>Sex:</b> Males.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 100 bp.<br>
  <b>Sample processing:</b> Articular cartilage mRNA profiles of 10-weeks-old wild-type (WT) and Oscar knockout (Oscar<sup>-/-</sup>) mice were performed for the destabilization of the medial meniscus (DMM) or sham surgery and each cartilage tissue sample was harvested from two time-points (2- and 4-weeks after surgery). Sequencing libraries were prepared according to the manufacturerâ€™s instructions (TruSeq Stranded mRNA Library Prep Kit; Illumina, San Diego, CA, USA).
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 2500 sequencer and normalized to TPM.
  
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_OA_sham_2weeks: Control mice, sham condition, 2 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>Wt_OA_sham_4weeks: Control mice, sham condition, 4 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>Wt_OA_dmm_2weeks: Control mice, DMM condition, 2 weeks after surgery (1 biological replicate, with 1 run each one).</li>
  <li>Wt_OA_dmm_4weeks: Control mice, DMM condition, 4 weeks after surgery (1 biological replicate, with 1 run each one).</li> 
  <li>OA_Oscar-ko_sham_2weeks: Mice knock out for Oscar, Oscar<sup>-/-</sup>, sham condition, 2 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_sham_4weeks: Mice knock out for Oscar, Oscar<sup>-/-</sup>, sham condition, 4 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_dmm_2weeks: Mice knock out for Oscar, Oscar<sup>-/-</sup>, DMM condition, 2 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
  <li>OA_Oscar-ko_dmm_4weeks: Mice knock out for Oscar, Oscar<sup>-/-</sup>, DMM condition, 4 weeks after surgery supplementation (1 biological replicate, with 1 run each one).</li>
</ul>

