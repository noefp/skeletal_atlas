<h3>Effects of disuse in bone by hindlimb unloading for 7 days</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of femorae after hindlimb unloading.<br>
  <b>Procedure:</b> Mice were subjected to hindlimb unloading for 7 days. Under isoflurane anesthesia, the mouse tail was taped to a freely rotating harness connected to a wheel that moved along the central axis of the custom-made cage. The harness was adjusted such that the mouse could not touch its hind limbs to the floor or the walls of the cage.<br>
  <b>Phenotype:</b> Decrease in bone mass and increase the risk of fracture.<br>
  <b>Tissue:</b> Femorae.<br>
  <b>Age:</b> 14 week old.<br>
  <b>Sex:</b> Females.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 50 bp.<br>
  <b>Sample processing:</b> After cutting the ends of bones and flushing marrow with PBS, the cortical bone was immediately snap-frozen in liquid nitrogen. 
  Subsequently, Trizol was added while the bone is maintained frozen in an eppendorf tube placed in a liquid nitrogen bath, homogenized in the same eppendorf tube with the Fastprep24 machine (MP Biomdicals), and total RNA was isolated and purified using manufacture recommendations for the PureLink RNA kit (Life Technologies). 
  RNA libraries were prepared for sequencing using standard Illumina protocols and samples were sequenced by PE sequencing with an Illumina HiSeq 2000 sequencer and normalized to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Disuse_Control: Control mice, not unloading hindlimbs (8 biological replicates, with 1 run each one).</li>
  <li>Disuse_HLU: Mice with hindlimbs suffering unloading (disuse) (8 biological replicates, with 1 run each one).</li>

</ul>

