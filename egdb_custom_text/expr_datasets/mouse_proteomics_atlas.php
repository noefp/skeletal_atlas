<h3>About</h3>
<p>
    This dataset was generated by our group.<br> 
    <br>
    We manually extracted different tissues from 12-week-old mice, except for cartilage, which was extracted from 3-day-old mice.<br>
    <br>
    Experimental procedure: 
    <br> 
    <ul>
        <li>Bone: Femora without bone marrow were pulverized in liquid nitrogen and transferred to an Eppendorf tube. RIPA buffer with protease inhibitors was added. The mixture was left at 4 <sup>o</sup>C for 30 minutes under agitation and then centrifuged. The supernatant was collected, transferred into a new tube, and stored at -80 <sup>o</sup>C until further analysis.</li>
        <br>
        <li>Cartilage from P3 pups was dissected and dissociated with collagenase. Once dissociated, collagenase was inactivated, and the samples were centrifuged at 4 <sup>o</sup>C. The pellet was resuspended in RIPA buffer containing protease inhibitors. After incubation at 4 <sup>o</sup>C, the samples were centrifuged again, and the supernatant was transferred to a new tube and snap-frozen in liquid nitrogen before being stored at -80 <sup>o</sup>C.</li>
        <br>
        <li>Bone marrow: Bone marrow was isolated from femora by centrifugation. The pellet was resuspended in ACK lysis buffer and then diluted with cold PBS. After centrifugation at 4 <sup>o</sup>C, the pellet was resuspended in RIPA buffer and centrifuged at 4 <sup>o</sup>C. The supernatant was transferred to a new tube, snap-frozen in liquid nitrogen, and stored at -80 <sup>o</sup>C until further analysis.</li>
        <br>
        <li>Tendon: Achilles tendons from two mice were extracted to obtain a biological replicate. Tendons were pulverized in liquid nitrogen and collected in a tube. RIPA buffer was added to the remains, and the samples were incubated at 4 <sup>o</sup>C for 30 minutes. Proteins were separated from debris by centrifugation. The supernatant was transferred to a new tube, snap-frozen in liquid nitrogen, and stored at -80 <sup>o</sup>C.</li>
        <br>
        <li>Muscle: Quadriceps were pulverized in liquid nitrogen, and the remains were transferred to a tube. RIPA buffer with protease inhibitors was added, and the samples were incubated at 4 <sup>o</sup>C for 30 minutes. The samples were then centrifuged, and the supernatant was transferred to a new tube, snap-frozen in liquid nitrogen, and stored at -80 <sup>o</sup>C.</li> 
    </ul>
    <br>
    After protein extraction, samples were sent to the Instituto Maimonides de Investigacion Biomedica de Cordoba (IMIBIC) where the Mass Spectrometry and Molecular Imaging unit proceeded to perform the LC-MS analysis for protein detection.

</p>
