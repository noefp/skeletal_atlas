<h3>Implant-Associated Osteomyelitis (IAOM) model in mouse</h3>
<p>
  <b>Experimental condition:</b>Transcriptional analysis of femur from an Implant-Associated Osteomyelitis (IAOM) model.<br>
  <b>Surgical procedure:</b> Orthopedic stainless pin was surgically placed in the right femoral midshaft of mice, followed by an inoculation of <i>Staphylococcus aureus</i> into the medullary cavity.<br>
  <b>Phenotype:</b> Typical characteristics of IAOM, like periosteal reaction and intraosseous abscess, occurred by day 14 postinfection. By day 28 postinfection, necrotic abscess, sequestrum formation, and deformity of the whole femur were observed.<br>
  <b>Tissue:</b> Femora.<br>
  <b>Age:</b> 10 week old.<br>
  <b>Sex:</b> Males.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 150 bp.<br>
  <b>Sample processing:</b> The right femur was collected and pulverized in liquid nitrogen and total RNA was extracted by TRIzol. Sequencing libraries were generated using NEBNext Ultra RNA Library Prep Kit for Illumina (NEB, USA) following manufacturer's protocols and index codes were added to attribute sequences for each sample. PCR products were purified using AMPure XP system (Beckman Coulter, USA) and library quality was assessed on the Agilent Bioanalyzer 2100 system. The clustering of the index-coded samples was performed on a cBot Cluster Generation System using TruSeq PE Cluster Kit v3-cBot-HS (Illumia) according to the manufacturer's instructions. 
  After cluster generation, the library preparations were sequenced on an Illumina NovaSeq 6000 and 150 bp PE reads were generated and normalized to TPM.
 
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Osteomyelitis_Control_3days: Control mice, analyzed after 3 days (3 biological replicates, with 1 run each one).</li>
  <li>Osteomyelitis_Control_14days: Control mice, analyzed after 14 days (3 biological replicates, with 1 run each one).</li>
  <li>Osteomyelitis_IAOM_3days: Mice infected with S.aureus by IAOM, analyzed after 3 days (3 biological replicates, with 1 run each one).</li>
  <li>Osteomyelitis_IAOM_14days: Mice infected with S.aureus by IAOM, analyzed after 14 days (3 biological replicates, with 1 run each one).</li>
</ul>

