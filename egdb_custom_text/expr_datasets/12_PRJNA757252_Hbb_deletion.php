<p>
  Condition: Transcriptome analysis of the effects of Hbb gene in cartilage and its function in hypoxia adaption
  Genotype: Hbb -/- (conditionally induced since Hbb-/- mice die at fetal stage) 
  Effects in tissue: Mice lacking of Hbb gene showed slow growth, shorter length, delayed cartilage hipertrophy with few pattern deffects
  Tissue: Knee cartilaginous tissues
  Age: P6
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Hbb_Wt: Control mice (3 biological replicates, with 1 run each one).</li>
  <li>Hbb_ko: Mice lacking of Hbb gene, Hbb -/- (3 biological replicates, with 1 run each one).</li>

</ul>

