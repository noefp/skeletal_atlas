<h3>Role of Hbb in cartilage</h3>
<p>
  <b>Experimental condition:</b> Transcriptional analysis of the effects of Hbb gene in cartilage and its function in hypoxia adaption.<br>
  <b>Gene:</b> Hbb gene codifies one of the two types of polypeptide chains in adult hemoglobin.<br>
  <b>Phenotype:</b> Mice lacking of Hbb gene showed slow growth, shorter length, delayed cartilage hipertrophy with few pattern deffects.<br>
  <b>Tissue:</b> Knee cartilaginous tissues.<br>
  <b>Age:</b> P6.<br>
  <b>Sex:</b> Both.<br>
  <b>Layout:</b> Paired.<br>
  <b>Reads length:</b> 150 bp.<br>
  <b>Sample processing:</b> The cartilaginous tissues were collected and lysed in TRIzol (Invitrogen) for RNA isolation according to the manufacturerâ€™s standard protocol. 
  An Agilent Bioanalyzer 2100 (Agilent Technologies) was used to check the integrity of the extracted and purified RNA. The TruSeq RNA sample preparation kit (Illumina) was used to generate the libraries. 
  Libraries were sequenced using an Illumina HiSEq 2500 sequencer and normalized to TPM.

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_Hbb: Control mice (3 biological replicates, with 1 run each one).</li>
  <li>Hbb_cko: Mice lacking of Hbb gene, Hbb<sup>-/-</sup> (3 biological replicates, with 1 run each one).</li>

</ul>

