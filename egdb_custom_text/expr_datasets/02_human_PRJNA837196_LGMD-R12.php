<p>
  Condition: Transcriptome analysis from muscles obtained from healthy donors and patients suffering Limb-girdle muscular dystrophy R12 (LGMD-R12)
  Tissue: Three different thigh muscles that are severely (semimembranosus), moderately (vastus lateralis) or mildly (rectus femoris) affected in LGMD-R12
  Age: Range from 26-64 year old 
  Sex: Men

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Healthy_patient: Muscles from healthy donors (26 biological replicates, with 4 runs each one).</li>
  <li>LGMD-R12_patient: Muscles from patients suffering LGMD-R12 (49 biological replicates, with 4 runs each one).</li>
  
</ul>

<p>
  RNA was extracted from muscle biopsies using the Direct-zol RNA Miniprep Plus kit ZY-R2073 (Zymo Research, Irvine, CA, USA) according to the kitâ€™s instructions.
  Samples were treated with DNase prior to their submission to the VIB Nucleomics Core (Leuven, Belgium, www.nucleomics.be). The Nanodrop ND-1000 (Nanodrop Technologies, Wilmington, DE, USA) was used to measure RNA concentration and purity spectrophotometrically. A Bioanalyzer 2100 (Agilent, Santa Clara, CA, USA) was applied to assess RNA integrity.
  Libraries were prepared using SMART-Seq Stranded Kit (Takara Bio USA, San Jose, CA, USA) and sequenced by PE sequencing using an Illumina NovaSeq 6000 sequencer and normalized to TPM.
</p>
