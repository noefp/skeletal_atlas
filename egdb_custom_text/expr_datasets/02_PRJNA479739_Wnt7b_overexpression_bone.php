<h3>Overexpression of Wnt7b in bone</h3>
<p>
  <b>Experimental condition:</b>Transcriptional analysis of Wnt7b overexpression in bone.<br>
  <b>Genes:</b>Wnt7b, a member of the Wnt signalling pathway. These genes are implicated in oncogenesis and in several developmental processes. Wnt7b shows the most profoundly anabolic effects on bone.<br>
  <b>Phenotype:</b>Mice overexpresing Wnt7b showed better bone formation.<br>
  <b>Tissue:</b>Femora.<br>
  <b>Age:</b>8 week old.<br>
  <b>Sex:</b>Both.<br>
  <b>Layout:</b>Paired.<br>
  <b>Reads length:</b>120 bp.<br>
  <b>Sample processing:</b>Total mRNA of femur with marrow flushed was collected from 8-week-old OE and Ctrl mice respectively using the RNeasy mini kit (Qiagen, Valencia, CA) according to the manufacturerâ€™s protocol.
  Libraries were prepared according to Illumina's instructions of RNA sample. Paired-end method was adopted for further processes.
  cDNA libraries were sequenced by PE sequencing with an Illumina HiSeq 2500 sequencer and normalized to TPM.
</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Wt_Wnt7b: Control mice, R26-Wnt7b (3 biological replicates, with 1 run each one).</li>
  <li>OE_Wnt7b: Mice overexpressing Wnt7b, 9.6kb DMP1<sup>Cre</sup>R26Wnt7b (3 biological replicates, with 1 run each one).</li>
</ul>

