<p>
  Condition: Transcriptome analysis of cartilage from healthy donors and patients suffering osteoarthritis (OA)
  Tissue: Knee Articular Cartilage
  Age: Ranging from 18 to 82
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Healthy.cartilage: Articular cartilage from healthy donors (18 biological replicates, with 1 run each one).</li>
  <li>OA.cartilage: Articular cartilage from patients suffering osteoarthritis (OA) (20 biological replicates, with 1 run each one).</li>
</ul>

<p>
  Cartilage was stored at −20°C in Allprotect Tissue Reagent (Qiagen, Valencia, CA) immediately after harvest until RNA extraction. For RNA isolation, cartilage was pulverized using a 6770 Freezer/Mill Cryogenic Grinder (SPEX SamplePrep, Metuchen, NJ), and homogenized in Qiazol Lysis Reagent (Qiagen, Valencia, CA). To remove proteins and cellular debris, a initial phenol-chloroform extraction was performed. 
  RNA purity was assessed using NanoDrop (ND-1000, Thermo Scientific, Wilmington, differentially expressed (DE) genes) and RNA integrity number (RIN) was calculated using a 2100 Bioanalyzer (Agilent, Santa Clara, CA). Libraries were prepared using the Encore Complete RNA-Seq DR Multiplex System 1–8 and 9–16 (NuGen, San Carlos, CA) with 16 unique indexed adapters (L2V6DR-BC2-L2V6DR-BC16).
  cDNA libraries were sequenced by SE sequencing with an Illumina HiSeq 2000 sequencer and normalized to TPM
</p>
