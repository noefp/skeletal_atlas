<p>
  Condition: Transcriptome analysis from articular cartilage from healthy and osteoarthritic cartilage
  Tissue: Knee articular cartilage
  Age: Range from 25-79 year old
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Healthy_cartilage: Patients with healthy cartilage (6 biological replicates, with 1 run each one).</li>
  <li>Osteoarthritic_cartilage:  Patients suffering Osteoarthritis (OA) (63 biological replicates, with 1 run each one).</li>
  
</ul>

<p>
  Patients fulfilled the American College of Rheumatology classification criteria for OA of the knee and had no history of knee injury, surgery, rheumatoid arthritis, or pseudogout. 
  Total RNA was isolated from homogenized cartilage using the RNeasy Fibrous Tissue Mini Kit (Qiagen) according to the manufacturer's instructions. The RNA concentration and purity were determined using a NanoDrop spectrophotometer (NanoDrop Technologies). 
  The integrity of the RNA was assessed in an Agilent Bioanalyzer 2100. Samples were prepared for RNA sequencing using BRB-seq protocol following manufacturer's protocols. 
  The cDNA libraries were sequenced by PE sequencing using an Illumina HiSeq 3000 sequencer and normalized to TPM.
</p>
<p>
  This dataset was published by <a href=" https://doi.org/10.1002/art.42369" target="_blank">Liao S et al., 2022</a> 
