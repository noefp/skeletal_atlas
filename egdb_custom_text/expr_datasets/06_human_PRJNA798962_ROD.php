<p>
  Condition: Transcriptome analysis from patients suffering Chronic Kidney Disease (CKD) with Low Bone Remodeling (LR) and High Bone Remodeling (HR) Renal Osteodystrophy (ROD)
  Tissue: Iliac crest bone
  Age: Middle age
  Sex: Both

</p>

<h4>Experimental Conditions:</h4>
<ul>
  <li>Healthy.ROD: Bone biopsies from healthy donors (9 biological replicates, with 1 run each one).</li>
  <li>LR.ROD: Bone biopsies of patients suffering CKD with Low Bone Remodeling (LR) Renal Osteodystrophy (ROD) (9 biological replicates, with 1 run each one).</li>
  <li>HR.ROD: Bone biopsies of patients suffering CKD with High Bone Remodeling (HR) Renal Osteodystrophy (ROD) (11 biological replicates, with 1 run each one).</li>
</ul>

<p>
  Iliac crest bone biopsies from 9 healthy volunteers and 20 patients with CKD, segregated by bone histomorphometry in low (9 biopsies) or high (11 biopsies) bone remodeling groups.
  RNA was isolated from bone using TRI reagent (Waltham, MA, USA) and purified using RNeasy kit (Qiagen, Germantown, MD, USA). Libraries were prepared according to Illumina's instructions, using the TruSeq Total RNA-Seq Library Preparation Kit (Illumina, San Diego, CA), 
  and the bar-coded cDNA libraries were sequenced by SE sequencing using an Illumina HiSeq 4000 sequencer and normalized to TPM.
</p>
<p>
  This dataset was published by <a href="https://doi.org/10.1172/JCI159928" target="_blank">Martinez-Calle M et al., 2023</a> 
  and raw data can be found in the <a href="https://www.ncbi.nlm.nih.gov/bioproject/?term=PRJNA798962" target="_blank">BioProject PRJNA798962</a>.
</p>
