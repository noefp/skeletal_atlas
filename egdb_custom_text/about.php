  <!-- <h2>About Skeletal Atlas</h2> -->

  <p style="font-size:18px;margin-top:20px;" class="text-justify">
        <p>
         The most comprehensive tool to analyze and extract public results on transcriptomic studies on skeletal tissues. 
        </p>
        <p>
          This atlas allows quickly exploration and generation of publication quality figures from data representing specific RNAseq musculoskeletal tissues (cortical bone, trabecular bone, skeletal muscle, growth plate, articular cartilage, periosteum, perichondrium, tendon and ligaments).
        </p>
        <p>
           The skeletal atlas initially incorporates mouse and human skeletal tissues for healthy and pathological conditions of the skeleton, as well as every treatment analyzed by transcriptomic approaches.
        </p>
        <p>
           To truly generate a comprehensive dataset we implement a tissue localization interface integrating each transcriptomic data within individualized tissue compartments extrapolated from localization studies of well-described markers.
        </p>
        <p>
           Through the features of this atlas the user will be able to performe the following analysis:
        </p>
<ul>
  <li>Compare expression data among tissues and specific compartments.</li>
  <li>Clustering based on known biomarkers: Transcriptomic consultation of well described cell populations (Hypertrophic chondrocytes, osteoclasts, etc)</li>
  <li>Detection of new biomarkers for skeletal conditions.</li>
  <li>Co-expression analysis of groups of genes involved in similar processes.</li>
  <li>Find enriched metabolic pathways and Gene Ontology terms in a selection of a cell population.</li>
  <li>Representation of the localized expression in a skeletal multi-tissue illustration (from cloud data imaging to in-situ tissue atlas illustration).</li>
  <li>Gene expression data comparison of multiple datasets for custom bioinformatic studies.</li>
  <li>Integration and representation of new skeletal expression datasets from open public databases.</li>
</ul>
        <p>
          Please contact us if you are interested.
        </p>
  </p>
